## List 생성 ------------------------------------------------------------------------------------------
nums1=[10,20]
nums2=list([10,20])

print(f'nums1의 ID : {id(nums1)}')
print(f'nums1[0]의 id : {id(nums1[0])}')
print(f'nums1[1]의 id : {id(nums1[1])}')
print('\n')
print(f'nums2의 ID : {id(nums2)}')
print(f'nums2[0]의 id : {id(nums2[0])}')
print(f'nums2[1]의 id : {id(nums2[1])}')