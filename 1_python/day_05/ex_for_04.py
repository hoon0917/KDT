# [실습] 2단 ~ 9단까지 모두 입력 받아서 구구단을 출력하세요
## dan : 2~9
for dan in range(2,10) :
    print(f'{dan}단',)
    for d in range(1,10) :
        print(f'{dan} * {d} = {dan*d}')

        
        
